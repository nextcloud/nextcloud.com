		<div class="row featurerow">
			<div class="col-md-4 featureblock">
				<img class="thumbnail" alt="Access" src="<?php bloginfo('template_directory'); ?>/assets/img/features/sharing.png" />
				<h3 class="featuretitle">Federated Cloud</h3></br>
				<p class="featuredesc">In previous versions of Nextcloud, users on separate Nextcloud servers could collaborate by sending public links. These links then offered a recipient the option of permanently mounting this public link in Nextcloud, something referred to as server to server sharing. In Nextcloud 8.0, the process for setting up server to server shared folders is now as easy as sharing a file with another Nextcloud user. This makes it easier than ever for federated Nextclouds to work together across servers, and even, if desired, organizations to collaborate and get work done.</p>
			</div>
			<div class="col-md-4 featureblock">
				<img class="thumbnail" alt="Sharing" src="<?php bloginfo('template_directory'); ?>/assets/img/features/favorites.png" />
				<h3 class="featuretitle">Favorites/Metadata support</h3></br>
				<p class="featuredesc">With ten thousand files or more found on a typical users file sync and share account, files can be very hard to find in a standard file system view. With file and folder favorites, users can now assign a favorite icon to files and folders that are important on the server, and be able to quickly locate, access and edit important content. More importantly, server favorites set a foundation for additional file and folder meta data in future releases of Nextcloud.</p>
			</div>
			<div class="col-md-4 featureblock">
				<img class="thumbnail" alt="Design" src="<?php bloginfo('template_directory'); ?>/assets/img/features/search.png" />
				<h3 class="featuretitle">Improved Search</h3></br>
				<p class="featuredesc">When working with files, users typically remember the files they have been working with in the last week or month, but older files tend to be harder to remember. Inevitably, there comes a time when even the best organization of files can't help a user find the content they need quickly. This is why we overhauled the search interface, providing a simpler and easier user experience, including enhanced result set reporting, additional search parameters and general usability improvements to make it easier for users to find the files they need fast.</p>
			</div>
		</div>

		<div class="row featurerow">
			<div class="col-md-12 featureblock">
				<img class="thumbnail" alt="something" src="<?php bloginfo('template_directory'); ?>/assets/img/features/design.png" />
				<h3 class="featuretitle">Web interface enhancements</h3></br>
				<p class="featuredesc">File sync and share has become dominant as a form of ad-hoc collaboration because it is simple to understand, easy to use, and just works. For this reason, the accessibility and usability of the Nextcloud interface has been improved to make it easier for all users to access, edit, sync and share their files.</p>
