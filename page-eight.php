				<p class="featuredesc">Integrating with LDAP and AD directories for user management is one of the most critical capabilities required for file sync and share solutions. Nextcloud has significantly improved LDAP and AD performance, provided additional configuration options and expert modes, and added a new utility for verifying and removing users from Nextcloud that are no longer active in LDAP or AD, making it easier to maintain Nextcloud in the enterprise.</p>
