				<p class="featuredesc">As the Nextcloud ecosystem grows, more and more apps are available to add to the server. Over time, this can make it more difficult to manage and administer the Nextcloud server. To solve this problem, a new user interface has been implemented for the Nextcloud Admin and Settings pages to make configuration of the Nextcloud server faster and easier for the administrator, including a new sidebar for organization and accessing app configuration options.</p>
