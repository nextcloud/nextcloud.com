		</div>
		<div class="row featurerow">
			<div class="col-md-12 featureblock">
				<img class="thumbnail" alt="something" src="<?php bloginfo('template_directory'); ?>/assets/img/features/client.png" />
				<h3 class="featuretitle">Download Broker</h3></br>
				<p class="featuredesc">When Nextcloud delivers universal file access to end users, files from many different document sources are aggregated into a single interface and served to end users. In some cases, passing all of the files aggregated in this interface through a single server, Nextcloud, can slow down data access. Nextcloud now supports direct downloads of files from select storage back-ends, reducing the load on the Nextcloud server without sacrificing control over the files that are stored in the various back end systems.</p>
