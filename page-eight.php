				<p class="featuredesc">As the number of available Nextcloud server apps grow, it can be difficult for administrators to locate, install, upgrade and remove apps. With this feature, Nextcloud now makes it easier on the administrator to install, upgrade and remove apps, and provides a new app dependency capability so that configuring and managing Nextcloud is easier than ever.</p>
