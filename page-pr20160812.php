<h1>Press event at IFA 2016: Using everyday technology to make a positive impact</h1>
<p><strong>Stuttgart, Germany, August 12, 2016</strong> Nextcloud joins forces with other socially-minded enterprises to put sustainability and ethical
business practices higher on the agenda of electronics industry.</p>

<p>The upcoming IFA trade show from 2 to 7 September in Berlin provides an excellent opportunity
to discuss how technology that has become a part of our everyday life affects people and the
planet and how it can be used to make a positive impact.</p>

<p>Consumers’ powerful role in influencing industry practices will be a key point of discussion
during a special breakfast session on Monday, 5 September. You are warmly invited to start
your day with Fairphone and four other socially-focused businesses that are using mainstream
consumer technology to create a positive impact, including:</p>

<ul>
	<li><a href="https://www.fairphone.com/" target="_blank">Fairphone</a> (Miquel Ballester, Co-founder): Creators of the modular, repairable Fairphone 2 – a phone that’s designed to last longer while making positive improvements throughout the supply chain.</li>
	<li><a href="http://littlesun.com/" target="_blank">Little Sun</a> (Felix Hallwachs, Managing Director): Designers of delightful solar-powered products who are using sustainable energy to transform lives – especially those without access
	to electricity.</li>
	<li><a href="http://ifixit.org/" target="_blank">iFixit</a> (Matthias Huisken, Director of iFixit Europe): A wiki-based website that teaches people
	how to fix almost anything and empowers individuals to share their technical knowledge with the rest of the world.</li>
	<li><a href="https://www.ecosia.org/" target="_blank">Ecosia</a> (Fátima Gonzales-Torres, Head of Community): An innovative search engine that’s making the world a better place by using ad revenue to plant trees (around 5 million to date).</li>
	<li><a href="https://nextcloud.com" target="_blank">Nextcloud</a> (Frank Karlitschek, Founder): A secure, open source cloud platform that gives businesses and individuals more control over their data.</li>
</ul>

