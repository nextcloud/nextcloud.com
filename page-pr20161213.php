	<h1>Nextcloud 11 sets new standard for security and scalability</h1>
	<p>Stuttgart, December 13, 2016 - Nextcloud GmbH, the company behind the most popular privacy-aware open source file sync and share technology announces the immediate availability of Nextcloud 11.</p>

	<p>This release introduces significant security improvements, attested by NCC Group, a global expert in cyber security and risk mitigation, to "complement the existing security architecture within the design" and "enhance the general standing of the security working environment."</p>

	<p>New security capabilities include:
	<ul>
	<li>support for cutting edge browser security features CSP 3.0 and Same-site Cookies</li>
	<li>support for Kerberos authentication and Two-factor Authentication providers based on Universal 2nd Factor and Time-based One-Time Password</li>
	<li>expanded brute force protection to all API access points</li>
	<li>more secure Federation through use of SSL/TLS</li>
	<li>access rights on app-specific login tokens</li>
	<li>Our new app store automatically checks apps and enforces signatures</li>
	</ul></p>

	<p>With scalability a prime concern among Nextcloud's large enterprise customers, this release decreases the database load and lowers resource usage up to 60% for common server operations. Multi-bucket Object Store support, improved handling of previews and Collabora Online speed improvements enable scaling to greater numbers of users and files, decrease the server load and improve user experience.</p>

	<blockquote>"Nextcloud GmbH has become a fast growing company while the Nextcloud project is the largest and most active in its space"</blockquote> said Frank Karlitschek, Managing Director at Nextcloud GmbH. <blockquote>"This shows the trust customers place in a business offering a sustainable solution to real business needs"</blockquote>

	<p>To satisfy strong customer demand, Nextcloud 11 introduces Apache Solr powered Full Text Search, enabling users to find words or phrases in text, pdf and common office documents on internal, external, shared and encrypted storage. The Next Generation Federation technology introduces a central lookup server, enabling Nextcloud users to find each other irrespective of the server their account resides on. The experimental Spreed app integrates secure, peer to peer audio and video chat in Nextcloud.</p>

	<p>Learn more about Nextcloud 11 on <a href="https://nextcloud.com/?p=1332">our release blog</a>

