<h1>Nextcloud iOS app Free for Download on App Store</h1>

<p><strong>August 24, Berlin, Germany.</strong> Nextcloud GmbH, the leading global supplier of open source enterprise sync, share and communication technology, announces at the annual Nextcloud Conference that the Nextcloud iOS will be a free download from the Apple App store going forward. This move is part of a strong focus on enterprise business, with support contracts the main driver for Nextcloud GmbH's profitability.</p>

<p><blockquote>Nextcloud aims to empower community to take back control over their data, not monetize them.</blockquote> said Frank Karlitschek, Managing Director of Nextcloud GmbH. <blockquote>Making the iOS app free, like the Android and desktop apps, fits in our strategy to focus on providing excellent enterprise support.</blockquote></p>

<p>Late 2016 Nextcloud announced open sourcing the iOS app, putting the full code under the GPL license with an App Store exception in github. Tomorrow, August 25, the Nextcloud Conference Focus Friday will start with sessions around scalability, security and collaboration in large enterprise organizations. The conference team expects well over 150 attendees in the audience.</p>

<p><blockquote>The Nextcloud iOS client has progressed tremendously in the last year</blockquote> said Marino Faggiana, iOS engineer at Nextcloud. <blockquote>removing the price tag means even more people have access to our private cloud solution!</blockquote></p>

<p><strong>About Nextcloud</strong><br />
