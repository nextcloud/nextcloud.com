<h1>Nextcloud Talk provides fully self-hosted, complete audio/video and text communication platform</h1>

<p><strong>January 10 2018, Stuttgart</strong> – Nextcloud, the globally leading supplier of self-hosted productivity solutions, delivers with Nextcloud Talk the first enterprise-ready, self-hosted communication technology for organizations providing the highest degree of control over their data and communication. Nextcloud Talk is a fully open source video meeting software, on-premises hosted and end-to-end encrypted. It features a text chat and is available for web and mobile.</p>

<p><blockquote>Nextcloud Talk lowers the barrier to self-hosting a privacy-respecting audio/video communication service for home users and enterprises alike</blockquote> said Frank Karlitschek, Managing Director at Nextcloud GmbH. <blockquote>Business users have optional access to the Spreed High Performance Back-end offering enterprise-class scalability, reliability, and features through a Nextcloud subscription.</blockquote></p>

<p>The efficient and intuitive interface of Nextcloud Talk enables businesses to engage customers or partners and empower employees to build better relationships, working with colleagues in real time through High Definition (H265 based) audio and video in web meetings and webinars. Main features of Nextcloud Talk include:</p>

<p><ul>
<li>Communication without barriers - the mobile apps for Android and iOS as well as browser support allow private and public calls, and does not require conversation partners to have an account or install any software to join a call.</li>
<li>Integrated in business workflow - participants can receive calendar invitations and reminders.</li>
<li>Easy collaboration - the Screen Sharing feature allows participants to present content while links and notes can be shared in the side chat bar.</li>
<li>Full control - the moderator can invite, mute and remove participants as needed.</li>
<li>100% secure and private - calls are peer-to-peer and end-to-end encrypted. Chat is exchanged encrypted with logs stored on the Nextcloud Talk server.</li>
<li>integration - powerful integration with Nextcloud Files and Nextcloud Groupware enables seamless collaboration.
</ul></p>

<p><blockquote>With the mobile Talk apps, users can now receive push notifications when others call them, and pick up easily</blockquote> said Mario Danic, Android engineer at Nextcloud. <blockquote>Self-hosting the infrastructure ensures the absolute confidentiality of your communication, as even metadata does not leak to anyone else.</blockquote></p>

<p>The Spreed High Performance Back-end for large organizations is offered by Nextcloud partner Struktur AG, a leading web conferencing software company since 1995, servicing customers like Deutsche Bank, Vodafone, BNP Paribas and many others. It offers improved connectivity behind firewalls, restrictive networks, and unlimited participants in a call. A SIP gate allows participants to join through a dial-in conference line, wherever they are.</p>

