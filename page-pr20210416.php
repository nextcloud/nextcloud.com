<h1>Managed Nextcloud: IONOS launches Professional Cloud Storage</h1>
<h3>Complete data sovereignty and German data protection / managed operation without administration effort / flexibly expandable</h3>
<em>Karlsruhe, April 16, 2021 - </em>With Managed Nextcloud, IONOS now offers a new professional cloud storage solution for small businesses, agencies and self-employed professionals. The DSGVO-compliant solution guarantees efficient teamwork in accordance with strict German data protection standards while enabling maximum flexibility when storing, managing and jointly editing data.

<blockquote>It is important for companies not to lose control of their own data when collaborating online and to be able to quickly and easily adapt their working environment to their own needs,</blockquote> says Achim Weiss, CEO of IONOS. <blockquote>With Managed Nextcloud, we offer our customers customized cloud storage that meets these requirements.</blockquote>

<h3>Collaboration without administrational effort</h3>

Managed Nextcloud promotes collaboration with colleagues and business partners via a central platform, while IONOS experts take care of updates, security and operation. Users have access to all data, which is automatically synchronized on all devices, anytime and anywhere. With the extended rights assignment, privately and shared documents, files and folders can be easily managed and assigned. In addition, companies can easily individualize their cloud storage with a personalized URL or the integration of the company logo - and thus further professionalize their presence.

<h3>Extend Nextcloud as required</h3>

The collaboration solution offers many customization options via an integrated open source app store. The applications and extensions include, for example, team calendars and address books, mail connection, document editor and a Kanban board. In addition, the integrated Talk app enables online meetings, video conferencing and screen sharing directly in your own cloud. Since all apps and extensions run directly via the web space, all data, conversations and content remain secure and confidential.

<h3>Data protection and complete data sovereignty</h3>

Managed Nextcloud meets the highest security standards in accordance with strict German data protection law. Data is stored exclusively in data centers in Germany. Access authorizations can be defined individually and activities can be tracked and logged in accordance with compliance guidelines. This facilitates reporting and auditing. In addition to desktop clients for Windows, Mac and Linux, there are mobile apps for all major platforms.

<h3>1 TB Managed Nexcloud storage three months free of charge</h3>

The "Managed Nextcloud 1 TB" plan with one terabyte of storage is recommended for up to 15 concurrent users and is free for the first three months. After that, the package costs 9 euros per month. The largest package, "Managed Nextcloud 10 TB," costs 45 euros per month and is recommended for up to 100 concurrent users. All tariffs and more information about Managed Nextcloud can be found here: <a href="https://www.ionos.de/office-loesungen/managed-nextcloud-hosting ">https://www.ionos.de/office-loesungen/managed-nextcloud-hosting </a>

