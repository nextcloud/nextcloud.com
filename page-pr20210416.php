<h1>Managed Nextcloud: IONOS startet professionellen Cloud-Speicher</h1>
<h3>Vollständige Datensouveränität und deutscher Datenschutz / Managed-Betrieb ohne Administrationsaufwand / flexibel erweiterbar</h3>
<em>Karlsruhe, 16. April 2021.</em> Mit Managed Nextcloud bietet IONOS jetzt einen neuen professionellen Cloud-Speicher für Kleinunternehmen, Agenturen und Selbständige. Die DSGVO-konforme Lösung garantiert effiziente Teamarbeit nach strengen deutschen Datenschutzstandards und ermöglicht gleichzeitig maximale Flexibilität beim Speichern, Verwalten und gemeinsamen Bearbeiten von Daten.

<blockquote>Für Unternehmen ist es wichtig, dass sie bei der Online-Zusammenarbeit die Kontrolle über ihre eigenen Daten nicht aus der Hand geben und ihre Arbeitsumgebung schnell und einfach ihren eigenen Bedürfnissen anpassen können</blockquote>, sagt Achim Weiß, CEO von IONOS. <blockquote>Mit Managed Nextcloud bieten wir unseren Kunden einen individuellen Cloud-Speicher, der diese Anforderungen erfüllt.</blockquote>

<h3>Kollaboration ohne Administrationsaufwand</h3>

Managed Nextcloud fördert die Zusammenarbeit mit Kollegen und Geschäftspartnern über eine zentrale Plattform, um Updates, Sicherheit und Betrieb kümmern sich die Experten von IONOS. Nutzer haben jederzeit und überall Zugriff auf sämtliche Daten, die auf allen Geräten automatisch synchronisiert werden. Mit der erweiterten Rechtevergabe lassen sich privat und gemeinsam genutzte Dokumente, Dateien und Ordner einfach verwalten und zuordnen. Zudem können Unternehmen ihren Cloud-Speicher durch eine personalisierte URL oder die Integration des Firmenlogos einfach individualisieren - und so ihren Auftritt weiter professionalisieren.

&nbsp;

<h3>Nextcloud nach Bedarf erweitern </h3>

Die Kollaborationslösung bietet über einen integrierten Open Source App Store viele Anpassungsmöglichkeiten. Zu den Anwendungen und Erweiterungen zählen zum Beispiel Team-Kalender und Adressbücher, Mail-Anbindung, Dokumenten-Editor sowie ein Kanban-Board. Außerdem ermöglicht die integrierte Talk-App Online-Meetings, Videokonferenzen und Screensharings direkt in der eigenen Cloud. Da alle Apps und Erweiterungen direkt über den Webspace laufen, bleiben alle Daten, Gespräche und Inhalte sicher und vertraulich.

<h3>Datenschutz und vollständige Datensouveränität</h3>

Managed Nextcloud genügt höchsten Sicherheitsstandards nach strengem deutschen Datenschutzrecht. Die Daten werden ausschließlich in Rechenzentren in Deutschland gespeichert. Zugriffsberechtigungen können individuell festgelegt und Aktivitäten können unter Einhaltung von Compliance-Richtlinien nachverfolgt und protokolliert werden. Dies erleichtert die Berichterstattung und Revision. Neben Desktop-Clients für Windows, Mac und Linux gibt es mobile Apps für alle wichtigen Plattformen.

<h3>1 TB Managed Nexcloud Speicher drei Monate kostenlos</h3>

Der Tarif "Managed Nextcloud 1 TB" mit einem Terabyte-Speicher wird für bis zu 15 gleichzeitige Nutzern empfohlen und ist die ersten drei Monate kostenlos. Danach kostet das Paket 9 Euro pro Monat. Das größte Paket "Managed Nextcloud 10 TB" kostet 45 Euro monatlich und wird für bis zu 100 gleichzeitige Nutzer empfohlen. Alle Tarife und weitere Informationen über Managed Nextcloud erhalten Sie hier: <a href="https://www.ionos.de/office-loesungen/managed-nextcloud-hosting">https://www.ionos.de/office-loesungen/managed-nextcloud-hosting</a>

