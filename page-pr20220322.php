<h1><strong>Nextcloud and MariaDB Collaborate to Boost Scalability and High Availability for Customers</strong></h1>
<p><em>Nextcloud customers will now be able to perform large deployments without hesitation and have a flexible, open source enterprise database</em></p>
<p><em>Stuttgart, March 22, 2022</em> - Nextcloud GmbH, the company behind the worlds’ most deployed on-premises content collaboration platform, and <a href="https://mariadb.com/" class="hyperlink">MariaDB® Corporation</a> announced a new partnership to bring Nextcloud customers a better database experience. With MariaDB Enterprise, users are guaranteed to have a reliable, scalable, high-performance database solution. Customers will be able to experience their data being utilized in collaborative applications like Nextcloud that fuel decision making across an organization.</p>
<blockquote>
<p>A database is a critical component for a Nextcloud setup, key for performance, scalability and reliability. MariaDB offers customers the certainty that their database will not be a bottleneck for the productivity of their employees.</p>
</blockquote>
<p>-- Frank Karlitschek, CEO and founder of Nextcloud GmbH</p>
<p>Nextcloud is deployed at a large scale at some of its customers, servicing millions of users in a single cluster. This puts significant strain on the database, requiring not only scalability and performance but also high availability and robust failover.</p>
<p>MariaDB Enterprise is the complete open source database solution. It supports transactional, analytical and combination workloads as well as relational, JSON and hybrid data models. MariaDB Enterprise, which includes MariaDB MaxScale, can deploy standalone databases and data warehouses to fully distributed SQL, which executes millions of transactions per second and performs interactive, ad hoc analytics on billions of rows.</p>
<blockquote>
<p>We’re excited to be partnering with Nextcloud to meet the ongoing needs from customers! Through our partnership, we’re making data readily available and even easier to access for applications and users with full security. With MariaDB Enterprise, Nextcloud customers are able to focus on growing their business without having to worry about the administrative overhead of infrastructure downtime, planned or unplanned.</p>
</blockquote>
<p>-- Jim Sears, VP Global Alliances, MariaDB</p>
<p>Nextcloud works closely with MariaDB to offer customers access to the best possible expertise. Customers can reach out to representatives from either company for more information.</p>
<h2>About Nextcloud</h2>
<p>Nextcloud Hub is the industry-leading, fully open-source, on-premises team productivity platform and Germany’s number one collaboration solution. It combines the easy user interface of consumer-grade cloud solutions with the security and compliance measures enterprises need. Nextcloud Hub brings together universal access to data through mobile, desktop and web interfaces with next-generation, on-premise secure communication and collaboration features like real-time document editing, chat and video calls, putting them under the direct control of IT and integrated with existing infrastructure.</p>
<p>Nextcloud’s easy and quick deployment, open, modular architecture and emphasis on security and advanced federation capabilities enable modern enterprises to leverage their existing file storage assets within and across the borders of their organization. For more information, visit <a href="https://nextcloud.com" class="hyperlink">nextcloud.com</a> or follow <a href="https://twitter.com/nextclouders" class="hyperlink">Nextclouders on Twitter</a>.</p>
<h2>About MariaDB Corporation</h2>
<p><a href="https://mariadb.com/" class="hyperlink">MariaDB</a> frees companies from the costs, cloud lock-in, constraints, and complexity of proprietary databases, enabling them to reinvest in what matters most – rapidly developing innovative, customer-facing applications. MariaDB uses pluggable, purpose-built storage engines to support workloads that previously required a variety of specialized databases. With complexity and constraints eliminated, enterprises can now depend on a single complete database for their needs, whether on commodity hardware or their cloud of choice. Deployed in minutes for transactional, analytical, or hybrid use cases, MariaDB delivers unmatched operational agility without sacrificing key enterprise features, including real ACID compliance and full SQL. Trusted by organizations such as Bandwidth, DigiCert, InfoArmor, Oppenheimer, Samsung, SelectQuote, SpendHQ – MariaDB meets the same core requirements as proprietary databases at a fraction of the cost. No wonder it’s one of the fastest-growing database management systems companies. <strong>Real business relies on MariaDB™</strong></p>
